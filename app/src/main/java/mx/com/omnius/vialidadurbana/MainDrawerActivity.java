    public void iniciaCarrusel(){
        HashMap<String,Integer> file_maps = new HashMap<String, Integer>();
        file_maps.put("Evita la diabetes",R.drawable.uno);
        file_maps.put("MÃ­dete no te pases",R.drawable.dos);
        file_maps.put("Higiene",R.drawable.tres);
        file_maps.put("Contra el fracaso escolar", R.drawable.cuatro);

        for (String name : file_maps.keySet()){
            TextSliderView textSliderView = new TextSliderView(this);
            textSliderView
                    .description(name)
                    .image(file_maps.get(name))
                    .setScaleType(BaseSliderView.ScaleType.Fit);

            sliderAuto.addSlider(textSliderView);
        }

        sliderAuto.setPresetTransformer(SliderLayout.Transformer.Fade);
        sliderAuto.setPresetIndicator(SliderLayout.PresetIndicators.Center_Bottom);
        sliderAuto.setCustomAnimation(new DescriptionAnimation());
        sliderAuto.setDuration(5000);
        sliderAuto.addOnPageChangeListener(this);
    }

